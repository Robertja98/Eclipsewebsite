      <h3>Cross-border contract language to watch for</h3>
      <p><strong>"Freight will be calculated based on current market rates and routing."</strong> — This means your shipping cost can change month-to-month with no cap. A local provider uses fixed routing and predictable cost.</p>
      <p><strong>"Emergency service available at a 50% premium."</strong> — If your DI tank runs empty on a Friday, you'll pay 50% extra to recover. Local service can respond without the emergency premium.</p>
      <p><strong>"Pricing in USD; customer absorbs currency fluctuation."</strong> — If the U.S. dollar strengthens, your bill increases automatically. This risk is invisible until exchange rates move.</p>
      <p><strong>"Minimum monthly commitment of $X; unused portion forfeited."</strong> — You're paying for capacity you don't always use. Local flat-rate pricing aligns cost with actual usage.</p>
