        <li><b>“Minimum monthly commitment of $X; unused portion forfeited.”</b> <br><span style="color:#555;">You’re paying for capacity you don’t always use. Local flat-rate pricing aligns cost with actual usage.</span></li>
      </ul>
      <h4 style="margin:1.5em 0 0.7em 0;">Key Questions to Ask</h4>
      <ul style="margin-left:1.2em;">
        <li>How is freight calculated? Is it per-mile, per-delivery, or included in the monthly fee?</li>
        <li>What are the border/customs costs? Ask for a year-to-date breakdown showing every customs or brokerage charge.</li>
        <li>Is pricing in CAD or USD? If USD, who absorbs currency loss?</li>
        <li>When does the “emergency” premium kick in? What constitutes after-hours or rush service?</li>
        <li>Are there per-delivery or per-tank-swap fees? Get specifics on every line item.</li>
