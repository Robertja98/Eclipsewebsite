      <h4 style="margin-bottom:0.7em;">Cross-Border Contract Language</h4>
      <ul style="margin-left:1.2em;">
        <li><b>“Freight will be calculated based on current market rates and routing.”</b> <br><span style="color:#555;">Shipping cost can change month-to-month with no cap. A local provider uses fixed routing and predictable cost.</span></li>
